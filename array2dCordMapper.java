
public class array2dCordMapper {

    public static void main(String[] args) {
        int n = 8;

        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                System.out.printf("(%d,%d) ", i, j);
            }
            System.out.println("\n");
        }
    }
}
